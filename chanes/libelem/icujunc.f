      SUBROUTINE ICUJ3(NG,P1,L1,P2,L2,P3,L3,B1,KNC2,NR,*)
C
C      ð/ð MAT. MOäEìé   éTõH'A âéð.TP-PA.
C          BEPOñTHO ðPéäETCñ BBECTé AððPOKCéMAãéà
C          HA âOìøûéX TOKAX.
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION P1,P2,P3
      DIMENSION P1(L1),P2(L2),P3(L3)
      DOUBLE PRECISION B1
      DIMENSION B1(KNC2,NR)
      DOUBLE PRECISION I0,AL,ALPHA,ARGMAX/40.0D0/

      UN1(U)=ALPHA*I0*(DEXP(AL*U)-1)
      ALPHA=P3(1)
      I0=P3(2)
      AL=P3(3)

      DO 10 K=1,KNC2,2
      U=B1(K,1)
      IF(AL*U.GT.ARGMAX) U=ARGMAX/AL
      B1(K,1)=UN1(U)
   10 B1(K+1,1)=0.0D0
      RETURN
C     DEBUG SUBTRACE,INIT(P1,L1,P2,L2,P3,L3,KNC2,NR,I0,AL,ALPHA,U)
      END


      SUBROUTINE ICUJ4(NG,P1,L1,P2,L2,P3,L3,B1,KNC2,NR,*)

C
C      CM. ð/ð ICUJ3
C

      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION P1,P2,P3
      DIMENSION P1(L1),P2(L2),P3(L3)
      DOUBLE PRECISION B1
      DIMENSION  B1(KNC2,NR)
      DOUBLE PRECISION I0,AL,ALPHA,ARGMIN/-120.0D0/,ARGMAX/40.0D0/
C                      $=MIN BEìéþéHA APçõMEHTA æ-ãéé EXP.

      DUN1(U)=ALPHA*AL*I0*DEXP(AL*U)
      ALPHA=P3(1)
      I0=P3(2)
      AL=P3(3)

      DO 10 K=1,KNC2,2
      U=B1(K,1)
      IF(AL*U.GT.ARGMAX) U=ARGMAX/AL
      B1(K,1)=0.0D0
      IF(U*AL.GT.ARGMIN) B1(K,1)= DUN1(U)
      B1(K+1,1)= 0.0D0
   10 CONTINUE
      RETURN
C     DEBUG SUBTRACE
      END


