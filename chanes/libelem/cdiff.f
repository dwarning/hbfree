      SUBROUTINE CDIFF3(NG,P1,L1,P2,L2,P3,L3,B1,KNC2,NR,*)
C
C      ð/ð MAT. MOäEìé AððPOKCéMAãéé äéææõúéOHHOê EMKOCTé
C
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION P1,P2,P3
      DIMENSION P1(L1),P2(L2),P3(L3)
      DOUBLE PRECISION B1
      DIMENSION B1(KNC2,NR)
      DOUBLE PRECISION TAU,I0,AL,ARGMIN/-120.0D0/,ARGMAX/40.0D0/

      C(U)=TAU*AL*I0*DEXP(AL*U)
      TAU=P3(1)
      I0=P3(2)
      AL=P3(3)
      DO 10 K=1,KNC2,2
      U=B1(K,1)
      B1(K,1)=0.0D0
      IF(U*AL.GT.ARGMAX) U=ARGMAX/AL
      IF(U*AL.GT.ARGMIN) B1(K,1)=C(U)*(1+AL*U)*B1(K,2)
      B1(K+1,1)=0.0D0
  10  B1(K+1,2)=0.0D0
      RETURN
C     DEBUG SUBTRACE,INIT(P1,L1,P2,L2,P3,L3,KNC2,NR,I0,AL)
      END


      SUBROUTINE CDIFF4(NG,P1,L1,P2,L2,P3,L3,B1,KNC2,NR,*)

C
C      CM. ð/ð CDIFF3
C

      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION P1,P2,P3
      DIMENSION P1(L1),P2(L2),P3(L3)
      DOUBLE PRECISION B1
      DIMENSION  B1(KNC2,NR)
      DOUBLE PRECISION I0,AL,ARGMIN/-120.0D0/,ARGMAX/40.0D0/
C                 $=MIN BEìéþéHA APçõMEHTA æ-ãéé EXP.
      C(U)=TAU*AL*I0*DEXP(AL*U)
      TAU=P3(1)
      I0=P3(2)
      AL=P3(3)
      DO 10 K=1,KNC2,2
      U=B1(K,1)
      IF(U*AL.GT.ARGMAX) U=ARGMAX/AL
      IF(U*AL.LE.ARGMIN) GO TO 20
      B1(K,1)=C(U)*AL*(2.D0+AL*U)*B1(K,2)
      B1(K,2)=C(U)*(1.D0+AL*U)
      GO TO 15
   20 CONTINUE
      B1(K,1)=0.D0
      B1(K,2)=0.D0
   15 CONTINUE
      B1(K+1,1)=0.D0
      B1(K+1,2)=0.D0
   10 CONTINUE
      RETURN
C     DEBUG SUBTRACE
      END


